private void hitungSelisihTanggal() {
    if (tglA == null || tglB == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Komponen tanggal tidak tersedia.");
        return;
    }
    java.util.Date a = tglA.getDate();
    java.util.Date b = tglB.getDate();
    if (a == null || b == null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Mohon isi kedua tanggal.");
        return;
    }
    LocalDate la = toLocalDate(a);
    LocalDate lb = toLocalDate(b);
    long diff = ChronoUnit.DAYS.between(la, lb);
    lblSelisih.setText("Selisih: " + Math.abs(diff) + " hari");
}

