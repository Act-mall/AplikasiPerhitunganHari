private LocalDate toLocalDate(java.util.Date d) {
    return d.toInstant().atZone(java.time.ZoneId.systemDefault()).toLocalDate();
}

private YearMonth getYearMonthFromInputs() {
    int bulan = cmbBulan.getSelectedIndex() + 1; // 1..12
    int tahun = ((Number) spnTahun.getValue()).intValue();
    return YearMonth.of(tahun, bulan);
}

private void syncToCalendar() {
    if (calBulan == null) return;
    java.util.Calendar c = calBulan.getCalendar();
    if (c == null) c = java.util.Calendar.getInstance();
    c.set(java.util.Calendar.YEAR, ((Number) spnTahun.getValue()).intValue());
    c.set(java.util.Calendar.MONTH, cmbBulan.getSelectedIndex()); // 0..11
    c.set(java.util.Calendar.DAY_OF_MONTH, 1);
    calBulan.setCalendar(c);
}

private void syncFromCalendar() {
    if (calBulan == null || calBulan.getCalendar() == null) return;
    java.util.Calendar c = calBulan.getCalendar();
    cmbBulan.setSelectedIndex(c.get(java.util.Calendar.MONTH));
    spnTahun.setValue(c.get(java.util.Calendar.YEAR));
}

