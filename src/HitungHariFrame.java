    private void initLogic() {
        cmbBulan.removeAllItems(); 
        // Masukkan semua bulan dari array
        for (String bulan : BULAN_ID) {
            cmbBulan.addItem(bulan);
        }

        // 2. Mengatur Spinner Tahun
        // Atur agar tahun memiliki rentang, misal 1900-2100
        // Ambil tahun saat ini
        int tahunIni = LocalDate.now().getYear();
        // Buat model spinner dengan nilai awal=tahunIni, min=1900, max=2100, step=1
        // Perlu import javax.swing.SpinnerNumberModel;
        SpinnerNumberModel modelTahun = new SpinnerNumberModel(tahunIni, 1900, 2100, 1);
        spnTahun.setModel(modelTahun);
        // Baris ini penting agar editor spinner tidak menggunakan format ribuan (misal "2.025")
        spnTahun.setEditor(new javax.swing.JSpinner.NumberEditor(spnTahun, "#"));


        // 3. Atur nilai awal berdasarkan tanggal hari ini
        LocalDate hariIni = LocalDate.now();
        cmbBulan.setSelectedIndex(hariIni.getMonthValue() - 1); // getMonthValue() -> 1..12, SelectedIndex -> 0..11
        // spnTahun sudah diatur ke tahunIni saat pembuatan model
        
        // 4. Atur JDateChooser dengan tanggal hari ini
        // Kita perlu konversi dari LocalDate ke java.util.Date
        java.util.Date tglUtilHariIni = java.sql.Date.valueOf(hariIni);
        tglA.setDate(tglUtilHariIni);
        tglB.setDate(tglUtilHariIni);

        // 5. Jalankan sinkronisasi dan perhitungan awal
        syncToCalendar();    // Sinkronkan JCalendar dengan ComboBox/Spinner
        hitungHariBulan(); // Jalankan perhitungan pertama kali agar label tidak kosong
        hitungSelisihTanggal(); // Inisialisasi label selisih
    }
